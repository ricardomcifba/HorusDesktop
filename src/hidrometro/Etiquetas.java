    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList lista = new ArrayList();
        File texto = new File(jLabel1.getText());
        boolean existe = texto.exists();
        FileReader fr = null;
        try {
            fr = new FileReader(texto);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Etiquetas.class.getName()).log(Level.SEVERE, null, ex);
        }
        BufferedReader br = new BufferedReader(fr);

        try {
            while (br.ready()) {

                //lÃª a proxima linha
                String linha = br.readLine();

                lista.add(linha);

            }
        } catch (IOException ex) {
            Logger.getLogger(Etiquetas.class.getName()).log(Level.SEVERE, null, ex);
        }
        BarCode.gerarLista(lista);
        JOptionPane.showMessageDialog(null, ("Etiquetas geradas com sucesso!!!"));

    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnCaminhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCaminhoActionPerformed
        JFileChooser fc = new JFileChooser();
        // restringe a amostra a diretorios apenas
        fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int res = fc.showOpenDialog(null);
        if (res == JFileChooser.APPROVE_OPTION) {
            File diretorio = fc.getSelectedFile();
            jLabel1.setText(diretorio.getPath());
        }
    }//GEN-LAST:event_btnCaminhoActionPerformed

